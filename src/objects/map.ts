// 32x32 map
export class Map {
  blocks: (Matrix4 | null)[][][] = Array(32)
    .fill(null)
    .map(() =>
      Array(32)
        .fill(null)
        .map(() => Array(4).fill(null))
    );

  constructor() {
    const body = new Cube();
    for (let x = 0; x < 32; x++) {
      for (let y = 0; y < 32; y++) {
        const mapValue = g_map[x][y];
        for (let z = 0; z < mapValue; z++) {
          //console.log(x,y);
          body.matrix.setIdentity();
          body.matrix.scale(0.5, 0.5, 0.5);
          body.matrix.translate(x - 16, -1 + z, y - 16);
          this.blocks[x][y][z] = new Matrix4().set(body.matrix);
        }
      }
    }
  }

  render() {
    const body = new Cube();
    body.color = [0.8, 1.0, 1.0, 1.0];
    body.setImage("dirt.jpg", 1);
    if (!g_normalOn) {
      body.textureNum = 1;
    } else {
      body.textureNum = -3;
    }
    for (let x = 0; x < 32; x++) {
      for (let y = 0; y < 32; y++) {
        for (let z = 0; z < 4; z++) {
          if (this.blocks[x][y][z] !== null) {
            body.matrix = this.blocks[x][y][z]!;
            body.render();
          }
        }
      }
    }
  }
}
